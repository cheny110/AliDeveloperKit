void OLEDDisplayTask(void const * argument)
{

    int stack1,stack2;
    size_t heap;
    GUI_SetDefaultBkColor(GUI_CYAN);
    GUI_Init();
    PROGBAR_Handle stackTask1ProgHandle,stackTask2ProgHandle,HeapProgHandle;
    GUI_SetBkColor(GUI_CYAN);
    GUI_Clear();
    GUI_SetColor(GUI_BLACK);
    GUI_SetFont(GUI_FONT_COMIC24B_ASCII);
    GUI_DispStringAt("Stack",240/2-50,10);
    stackTask1ProgHandle=PROGBAR_Create(30,40,180,20,WM_CF_SHOW);
    stackTask2ProgHandle=PROGBAR_Create(30,70,180,20,WM_CF_SHOW);
    GUI_DispStringAt("Heap",240/2-40,100);
    HeapProgHandle=PROGBAR_Create(30,130,180,20,WM_CF_SHOW);
    PROGBAR_SetSkinClassic(HeapProgHandle); //属性设置应在赋值之后
    PROGBAR_SetBarColor(HeapProgHandle,0,GUI_RED);
    PROGBAR_SetText(stackTask1ProgHandle,"Task1 Available");
    PROGBAR_SetText(stackTask2ProgHandle,"Task2 Available");
    PROGBAR_SetText(HeapProgHandle,"Heap Occupied");
    PROGBAR_SetMinMax(stackTask1ProgHandle,1,200*4);
    PROGBAR_SetMinMax(stackTask2ProgHandle,1,400*4);
    PROGBAR_SetMinMax(HeapProgHandle,0,10000);
    
    GUI_DispStringAt("Version:1.0",120-60,200);
    GUI_SetFont(&GUI_Font6x8_ASCII);
    GUI_DispStringAt("by Cheny",180,230);
    WM_Exec();
    

  for(;;)
  {
    stack1=uxTaskGetStackHighWaterMark(defaultTaskHandle)*4; //返回栈剩余
    stack2=uxTaskGetStackHighWaterMark(NULL)*4;     
    heap=xPortGetFreeHeapSize();
    PROGBAR_SetValue(stackTask1ProgHandle,stack1);
    PROGBAR_SetValue(stackTask2ProgHandle,stack2);
    PROGBAR_SetValue(HeapProgHandle,heap);
    
    GUI_Delay(100);

  }
}
